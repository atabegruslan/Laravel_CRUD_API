    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        try
        {
            $comment               = new Comment;
            $comment->entry_id     = $request->input('entry_id');
            $comment->contents     = $request->input('contents');
            $comment->commentor_id = $request->input('commentor_id');
            $comment->save();

            return response()->json(["comment" => $comment] , 200);
        }
        catch(Throwable $t)
        {
            return response()->json(["error" => $t->getMessage()] , 400);
        }
    }

    /**
     * Display the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show($id)
    {
        try
        {
            $comment = Comment::with('commentor')->where('entry_id', $id)->get();

